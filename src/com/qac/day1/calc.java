package com.qac.day1;

public class calc {
	
	public int addCalc(int a, int b) {
		
		int c = a+b;
		return c;
		
	}
	
	public int subCalc(int a, int b) {
		

		int c = a-b;
		return c;
		
	}


	public int multiCalc(int a, int b) {
		
		int c = a*b;
		return c;
		
	}
	
	public int diviCalc(int a, int b) {
		
		int c = a/b;
		return c;
		
	}
	
	public int comboCalc(int a, int b, int c) {
		
		int d = a%b;
		return d;
		
	}
	
}
